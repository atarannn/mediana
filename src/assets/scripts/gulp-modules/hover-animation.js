document.querySelectorAll('.card-style.js-hover-card-animation').forEach( card => {
  card.addEventListener('mouseover', () => {
    document.querySelector('.card-style.js-hover-card-animation .light-title').classList.add('visible');
    document.querySelector('.card-style.js-hover-card-animation .subtitle-text-20').classList.add('visible');
    document.querySelector('.card-style.js-hover-card-animation .btn').classList.add('visible');
  });
})

document.querySelectorAll('.card-style.js-hover-card-animation').forEach( card => {
  card.addEventListener('mouseout', () => {
    document.querySelector('.card-style.js-hover-card-animation .light-title').classList.remove('visible');
    document.querySelector('.card-style.js-hover-card-animation .subtitle-text-20').classList.remove('visible');
    document.querySelector('.card-style.js-hover-card-animation .btn').classList.remove('visible');
  });
})

